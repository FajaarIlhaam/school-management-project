    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
       String Nip=txtnip.getText();
       String Nama=txtnama.getText();
       String Mapel=cmbmapel.getSelectedItem().toString();
       
    if(Nip.equals("")| (Nama.equals("")) | (Mapel.equals(""))){
       JOptionPane.showMessageDialog(null, "Data tidak boleh kosong");
       txtnip.requestFocus();
       
    }else{
           try{
               String sql ="Insert into guru values (?,?,?)";
               PreparedStatement stat = konek.prepareCall(sql);
           try{
               stat.setString(1, Nip);
               stat.setString(2, Nama);
               stat.setString(3, Mapel);
               stat.executeUpdate();
               JOptionPane.showMessageDialog(null,"Data Guru tersimpan");
               tampiltabel();
           }catch(Exception e){
                  JOptionPane.showMessageDialog(null, "Ada kesalahan di" + e);
              }
           }catch(Exception se){
                  JOptionPane.showMessageDialog(null, "Ada kesalahan di" + se);
           }
       }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         koneksi();
        
        int ok=JOptionPane.showConfirmDialog(null,"Anda yakin ingin mengedit data " + txtnip.getText()+ "'","Yakin Ingin Edit data",JOptionPane.YES_NO_CANCEL_OPTION);                               
        
        if (ok==0){
        try{
            String sql="Update guru set nama='"+txtnama.getText()+"',mapel='"+cmbmapel.getSelectedItem().toString()+"'where nip='"+txtnip.getText()+"'";
            PreparedStatement stat = konek.prepareStatement(sql);
            try{
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null,"Data Berhasil di Update");
                tampiltabel();
                hapus();
            }catch(SQLException e) {
                JOptionPane.showMessageDialog (null, "Ada Kesalahan di" + e);
            }     
            }catch(SQLException se) {
                JOptionPane.showMessageDialog(null, "Ada Kesalahan di" + se);
            }       
        }
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

