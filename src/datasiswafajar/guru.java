void koneksi(){
    try{
         Class.forName("com.mysql.jdbc.Driver");
         System.out.println("Driver diketemukan");
     
       try{
           String url="jdbc:mysql://localhost/datasiswa_fajar?user=root&password=";
           konek=DriverManager.getConnection(url);
           JOptionPane.showMessageDialog (null,"Anda Berhasil Koneksi");
    
       }catch(SQLException e) {
               System.out.println("Gagal Koneksi"+ e);
               System.exit(0);   
       }    
       }catch (Exception se){
         System.out.println("Driver tidak ditemukan"+ se);
         JOptionPane.showMessageDialog (null,"koneksi tidak ditemukan...");
         System.exit(0) ;
     }
    } 
void hapus(){
    txtnip.setText("");
    txtnama.setText("");
 
}

void combomapel() {
    cmbmapel.addItem("");
    cmbmapel.addItem("PBO");
    cmbmapel.addItem("Basis Data");
    cmbmapel.addItem("PWPB");
    cmbmapel.addItem("PKN");
}

void cari(){
    koneksi();
    try{
        String sql="Select * from guru where nip ='"+txtnip.getText()+"'";
        Statement st =konek.createStatement();
        ResultSet rs=st.executeQuery(sql);
        while (rs.next()){
            txtnama.setText(rs.getString("nama"));
            cmbmapel.setSelectedItem(rs.getString("mapel"));
        }
    }catch(SQLException se) {
        JOptionPane.showMessageDialog (null,"ada kesalahan"+ se);        
    }
}
    
void hapustabel(){
    int row = tabMode.getRowCount();
    for (int i=0;i<row;i++){
        tabMode.removeRow(0);
    }
}

void tampiltabel(){
       koneksi();
       hapustabel();
       
       try{
           String SQL ="Select * from guru";
           Statement stat= konek.createStatement();
           ResultSet set = stat.executeQuery(SQL);
           while(set.next()){
               String Nip=set.getString("nip"); 
               String Nama=set.getString("nama"); 
               String Mapel=set.getString("mapel"); 
               String[] data={Nip,Nama,Mapel};
               tabMode.addRow(data);
           }  
       }catch(SQLException e){
           JOptionPane.showMessageDialog(null, "Ada kesalahan data di"+ e);
       }
}
